const messages: Record<Locale, Record<MessageKey, string>> = {
  en,
  'zh-CN': zhCN,
};

type Values = Record<string, string | number>;

interface I18nContextValue {
  locale: Locale;
  t: (key: MessageKey, values?: Values) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function resolveLocale(languages?: readonly string[] | null, language?: string | null): Locale {
  const candidates = languages && languages.length > 0 ? languages : language ? [language] : [];

  for (const candidate of candidates) {
    const normalized = candidate.toLowerCase();
    if (normalized === 'en' || normalized.startsWith('en-')) return 'en';
    if (normalized === 'zh' || normalized.startsWith('zh-')) return 'zh-CN';
  }

  return DEFAULT_LOCALE;
}

export function getBrowserLocale(): Locale {
  if (typeof navigator === 'undefined') return DEFAULT_LOCALE;
  return resolveLocale(navigator.languages, navigator.language);
}

export function translate(locale: Locale, key: MessageKey, values: Values = {}) {
  return messages[locale][key].replace(/\{(\w+)\}/g, (match, name) => {
    const value = values[name];
    return value === undefined ? match : String(value);
  });
}

export function I18nProvider({
  children,
  initialLocale,
}: {
  children: ReactNode;
  initialLocale?: Locale;
}) {
  const [locale] = useState<Locale>(() => initialLocale ?? getBrowserLocale());

  useEffect(() => {
    document.documentElement.lang = locale;
  }, [locale]);

  const t = useCallback((key: MessageKey, values?: Values) => translate(locale, key, values), [locale]);
  const value = useMemo(() => ({ locale, t }), [locale, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within I18nProvider');
  }
  return context;
}
